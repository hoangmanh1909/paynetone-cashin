package com.paynetone.counter.model;

public class OptionAmount {
    private int amount;
    private String text;

    public void setAmount(int amount) {
        this.amount = amount;
    }

    public void setText(String text) {
        this.text = text;
    }

    public int getAmount() {
        return amount;
    }

    public String getText() {
        return text;
    }
}
